#include "eRayCast.h"

eRayCast::eRayCast(const mVec3 &origin, const mVec3 &end, unsigned int mask, bool flag) {
    __asm__ volatile(
        "andi $t0, $t0, 0xff\n"
        "sw $a3, 0($a0)\n"
        "sw $zero, 4($a0)\n"
        "ori $a3, $zero, 0x1\n"
        "sb $a3, 24($a0)\n"
        "sb $zero, 25($a0)\n"
        "lui $a3, 0x7f7f\n"
        "ori $a3, $a3, 0xffff\n"
        "mtc1 $a3, $f12\n"
        "swc1 $f12, 32($a0)\n"
        "mtc1 $zero, $f12\n"
        "mfc1 $a3, $f12\n"
        "mfc1 $t1, $f12\n"
        "mfc1 $t2, $f12\n"
        "mtv $a3, S120\n"
        "mtv $t1, S121\n"
        "mtv $t2, S122\n"
        "sv.q C120, 0x30($a0)\n"
        "mfc1 $a3, $f12\n"
        "mfc1 $t1, $f12\n"
        "lui $t2, 0x3f80\n"
        "mtc1 $t2, $f13\n"
        "mfc1 $t2, $f13\n"
        "mtv $a3, S120\n"
        "mtv $t1, S121\n"
        "mtv $t2, S122\n"
        "sv.q C120, 0x40($a0)\n"
        "addiu $a3, $zero, -1\n"
        "sw $a3, 80($a0)\n"
        "sw $a3, 84($a0)\n"
        "sw $zero, 88($a0)\n"
        "sw $zero, 92($a0)\n"
        "lv.q C120, 0($a1)\n"
        "sv.q C120, 0x60($a0)\n"
        "lv.q C130, 0($a2)\n"
        "vsub.t C120, C130, C120\n"
        "sv.q C120, 0x70($a0)\n"
        "vdot.t S100, C120, C120\n"
        "vcmp.s ez, S100\n"
        "vrsq.s S101, S100\n"
        "vsqrt.s S102, S100\n"
        ".word 0xdc0010e5\n"  // vpfxs 1, Y, Z, W
        "vcmovt.s S101, S100, 0\n"
        "vscl.t C120, C120, S101\n"
        "sv.q C120, 0x70($a0)\n"
        "mfv $a1, S102\n"
        "mtc1 $a1, $f12\n"
        "swc1 $f12, 128($a0)\n"
        "sb $t0, 144($a0)\n"
    );
}
